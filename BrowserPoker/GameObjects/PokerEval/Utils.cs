                    case '3': rank = 1; break;
                    case '4': rank = 2; break;
                    case '5': rank = 3; break;
                    case '6': rank = 4; break;
                    case '7': rank = 5; break;
                    case '8': rank = 6; break;
                    case '9': rank = 7; break;
                    case 'T':
                    case 't': rank = 8; break;
                    case 'J':
                    case 'j': rank = 9; break;
                    case 'Q':
                    case 'q': rank = 10; break;
                    case 'K':
                    case 'k': rank = 11; break;
                    case 'A':
                    case 'a': rank = 12; break;
                    default:
                        return 0;
                }
                switch (s[i + 1])
                {
                    case 'C':
                    case 'c': suit = 0; break;
                    case 'D':
                    case 'd': suit = 1; break;
                    case 'H':
                    case 'h': suit = 2; break;
                    case 'S':
                    case 's': suit = 3; break;
                    default:
                        return 0;
                }
                result = result | (1UL << (rank + (suit * 13)));
            }
            return result;
        }

        public static string CardMaskToString(ulong mask)
        {
            var builder = new System.Text.StringBuilder();
            int count = 0;
            for (int i = 51; i >= 0; i--)
            {
                if (((1UL << i) & mask) != 0)
                {
                    if (count != 0)
                    {
                        builder.Append(" ");
                    }
                    builder.Append(CardTable[i]);
                    count++;
                }
            }
            return builder.ToString();
        }

        public static string CardMaskToStringSorted(ulong mask)
        {
            var list = new List<string>();
            for (int i = 0; i < CardMasksTable.Length; i++)
            {
                var card = CardMasksTable[i];
                if ((card & ~mask) == 0)
                    list.Add(CardTable[i]);
            }

            var sortedList = list.OrderBy(
                i => i[0] == 'K').ThenBy(
                i => i[0] == 'Q').ThenBy(
                i => i[0] == 'J').ThenBy(
                i => i[0] == 'T').ThenBy(
                i => i[0] == '9').ThenBy(
                i => i[0] == '8').ThenBy(
                i => i[0] == '7').ThenBy(
                i => i[0] == '6').ThenBy(
                i => i[0] == '5').ThenBy(
                i => i[0] == '4').ThenBy(
                i => i[0] == '3').ThenBy(
                i => i[0] == '2').ThenBy(
                i => i[0] == 'A').ToList();
            var result = string.Empty;
            foreach (var item in sortedList)
            {
                result += item + " ";
            }
            return result.Substring(0, result.Length - 1);
        }

